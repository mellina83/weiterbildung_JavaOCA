package basics_variablen;

public class B01_WarumVariablen {

	public static void main(String[] args) {

		
		int a = 20; // Speicher reservieren und einen Wert ablegen
		int b = 30; // Speicher reservieren und einen Wert ablegen
		
		int z = a + b; // Werte finden (a, b lesen), neue Zahl berechnen, Speicher reservieren (z) und den Wert ablegen  
		
		System.out.println( z ); // Wert finden (z lesen), 
		
		z = a * b; // Werte finden (a, b), neue Zahl berechnen, Speicher (z) wiederverwenden und den Wert ablegen
		
		System.out.println( z ); // Wert finden (z lesen),
		
	} // end of main

}
 